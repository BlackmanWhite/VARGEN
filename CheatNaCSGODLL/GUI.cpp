void GUI::Button(const char* label, bool* p_open, const ImVec2& size) noexcept
{
	if (*p_open)
	{
		ImGui::ButtonEx(label, size, 0);
		return;
	}

	if (ImGui::ButtonEx(label, size, 0) && !*p_open)
	{
		window.aimbot = false;
		window.antiaim = false;
		window.chams = false;
		window.colors = false;
		window.config = false;
		window.esp = false;
		window.misc = false;
		window.rage = false;
		window.triggerbot = false;
		window.visuals = false;
		*p_open = true;
	}
	
	return;
}

